# Code in python to generate the following:
# Even numbers from 1 (one) through 100 (one hundred)
# Odd numbers from 1 (one) through 100 (one hundred)
for i in range(0,100):
    if(i%2 == 0):
        print("this are the even numbers",i)
    elif(i%2 != 0):
        print("this are the odd numbers", i)